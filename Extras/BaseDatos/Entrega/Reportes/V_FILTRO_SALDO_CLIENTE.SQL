CREATE OR REPLACE FORCE VIEW V_FILTRO_SALDO_CLIENTE AS 
SELECT DEL.COD_DELEGACION,
          DEL.OID_DELEGACION,
          DEL.DES_DELEGACION,
          PLA.OID_PLANTA,
          PLA.COD_PLANTA,
          SEC.OID_SECTOR,
          SEC.COD_SECTOR,
          SEC.DES_SECTOR,
          CLI.OID_CLIENTE,
          CLI.COD_CLIENTE,
          CLI.DES_CLIENTE,
          SBC.OID_SUBCANAL,
          SBC.COD_SUBCANAL,
          SBC.DES_SUBCANAL,
          CAN.OID_CANAL,
          CAN.COD_CANAL,
          CAN.DES_CANAL,
          SCL.OID_SUBCLIENTE,
          SCL.COD_SUBCLIENTE,
          SCL.DES_SUBCLIENTE,
          PSE.OID_PTO_SERVICIO,
          PSE.COD_PTO_SERVICIO,
          PSE.DES_PTO_SERVICIO,
          CUE.OID_CUENTA
     FROM RPGE_YDELEGACION DEL
          INNER JOIN RPGE_YPLANTA PLA
             ON DEL.OID_DELEGACION = PLA.OID_DELEGACION
          INNER JOIN RPGE_YSECTOR SEC ON SEC.OID_PLANTA = PLA.OID_PLANTA
          INNER JOIN RPGE_YCUENTA CUE ON CUE.OID_SECTOR = SEC.OID_SECTOR
          INNER JOIN RPGE_YCLIENTE CLI ON CLI.OID_CLIENTE = CUE.OID_CLIENTE
          INNER JOIN RPGE_YSUBCANAL SBC
             ON SBC.OID_SUBCANAL = CUE.OID_SUBCANAL
          INNER JOIN RPGE_YCANAL CAN ON CAN.OID_CANAL = SBC.OID_CANAL
          LEFT OUTER JOIN RPGE_YSUBCLIENTE SCL
             ON     SCL.OID_CLIENTE = CUE.OID_CLIENTE
                AND SCL.OID_SUBCLIENTE = CUE.OID_SUBCLIENTE
          LEFT OUTER JOIN RPGE_YPUNTO_SERVICIO PSE
             ON     PSE.OID_SUBCLIENTE = CUE.OID_SUBCLIENTE
                AND PSE.OID_PTO_SERVICIO = CUE.OID_PTO_SERVICIO
/