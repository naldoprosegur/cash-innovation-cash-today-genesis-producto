INSERT INTO SAPR_TTEMP_UPDATE_FECHAS_MOV (FEC_MOVIMIENTO, NUM_TOTAL_MOVIMIENTOS, GMT_MODIFICACION)
SELECT DISTINCT MOVI.FEC_MOVIMIENTO, 0, SYSTIMESTAMP FROM SAPR_TMOVIMIENTO MOVI 
WHERE NOT EXISTS (SELECT TEMP.FEC_MOVIMIENTO FROM SAPR_TTEMP_UPDATE_FECHAS_MOV TEMP WHERE MOVI.FEC_MOVIMIENTO = TEMP.FEC_MOVIMIENTO) ORDER BY 1 ASC;
COMMIT;
